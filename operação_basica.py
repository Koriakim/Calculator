# Treinando função 'Input'

number_1 = int(input('Insira o primeiro numero: '))

number_2 = int(input('Insira o segundo numero: '))

# Todas as operações feitas atraves do print

print(f'{number_1} + {number_2} =', number_1 + number_2)
print(f'{number_1} - {number_2} =', number_1 - number_2)
print(f'{number_1} * {number_2} =', number_1 * number_2)
print(f'{number_1} / {number_2} =', number_1 / number_2)

